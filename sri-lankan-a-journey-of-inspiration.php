                                    <p><b><em>Anuradhapura, </em></b>situated in Sri Lanka's North Central Province, is
                                        a venerable city steeped in history, dating back over 2,500 years. As a former
                                        capital, it boasts awe-inspiring ancient stupas like Ruwanwelisaya and
                                        Jetavanaramaya, and the sacred Bodhi Tree, revered as a descendant of the
                                        Buddha's enlightenment tree. Anuradhapura is a UNESCO World Heritage Site,
                                        celebrated for its archaeological wonders and profound Buddhist heritage. Today,
