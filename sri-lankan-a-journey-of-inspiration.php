                                    <p><b><em>Mask making</em></b> is a popular tradition in Ambalangoda. These masks
                                        fall into three categories. They are known as ‘Kolam’, ‘Raksha’ and ‘Sanni’.
                                        ‘Kolam’ masks tells the mocking stories of traditional Sri Lankan colonial life.
                                        ‘Raksha’ mask is used in festivals and processions. And ‘Sanni’ masks are used
                                        to heal people of persisting illnesses believed to be inflicted by demons. </p>

                                    <p><b><em>Geoffrey Bawa</em></b> is Sri Lanka’s most prolific and influential
                                        architect. His work has had a tremendous impact upon architecture throughout
                                        Asia and is unanimously acclaimed by connoisseurs of architecture worldwide.
                                        Every turn and every bend in Bawa’s country home is an interesting experience –
                                        with so many exotic plants and trees and views to take in throughout the
                                        distinct homes and garden. The house itself is sparse but beautiful, full of
                                        impressive paintings and photographs of a bygone age.</p>
