                                <h5 class="company-name"><span style="color:#6b52a3;">Day 06 &nbsp;&nbsp;</span>
                                    Kandy</h5>
                                <div>
                                    <ul class="fa-ul">
                                        <li><i class="fa-li fa fa-hand-o-right"></i>After a delightful breakfast, set
                                            out on a captivating exploration of the Gladaladenya, Lankatilaka, and
                                            Embekka temples, immersing yourself in the rich architectural and cultural
                                            heritage of Sri Lanka. (Traveling time approximately 30 minutes)</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Overnight stay in Kandy.</li>
                                    </ul>
                                    <p><b><em>Gadaladeniya Temple, </em></b>constructed in 1344 by King Buwanekabahu IV,
                                        stands apart from traditional Sri Lankan Buddhist temples due to its Dravidian
                                        architectural influence and Oriental charm, featuring a unique roofed dagoba on
                                        four pillars, a seated Lord Buddha statue, four additional Buddha statues,
                                        intricate interior sculptures, frescoes depicting Buddha's stories, a Dragon
                                        Arch adorned with Gods Suyama, Sathusta, and Natha, and a shrine room dedicated
                                        to God Vishnu, making it a highly frequented attraction in Kandy for its
                                        distinctive architecture, ornate frescoes, and scenic location.</p>

                                    <p><b><em>Lankatilaka Temple, </em></b>dating back to the Gampola Kingdom era and
                                        constructed in 1344 AD by King Buwanekabahu IV, was designed by South Indian
                                        architect Sthapati Rayar, blending Sinhalese, Dravidian, and Indo-Chinese
                                        architectural styles according to the late Professor Paranavitana, featuring a
                                        Gedige-style Polonnaruwa architecture with a granite-based foundation on uneven
                                        rock terrain, and today, only the Ground Floor and part of the First Floor
                                        remain of the original four-story structure, adorned with paintings depicting
                                        the lives of the twenty-four previous Buddhas in the image house's surrounding
                                        walls.</p>

                                    <p><b><em>Embekke Devalaya</em></b> in Kandy, constructed by King Vikramabahu IV in
                                        the Gampola Kingdom era, is renowned for its exceptional wood carvings,
                                        considered the country's finest, and features three distinct areas: the Hevisi
                                        Mandapaya (Drummer’s Hall), Digge (Dancing Hall), and Garagha (sanctum),
                                        originally a three-story structure, now showcasing remarkable, intricate
                                        carvings that offer a glimpse into the exceptional architecture of that period.
