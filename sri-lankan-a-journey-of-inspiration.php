                                    <p><b><em>Galle Fort</em></b> in the Bay of Galle on the southwest coast of Sri
                                        Lanka, was built first in 1588 by the Portuguese, then extensively fortified by
                                        the Dutch during the 17th century from 1649 onwards. The heritage value of the
                                        fort has been recognized by the UNESCO and the site has been inscribed as a
                                        cultural heritage UNESCO World Heritage. Some of the important heritage
                                        monuments in the fort are the Dutch Reformed Church, with its historic belfry
                                        dated 1707 and cast in 1709, which rang every hour. The old Dutch government
                                        house, residence of the Commander, Great Warehouse, Old Dutch Hospital, Clock
                                        Tower and the Galle Lighthouse are some other monuments in Galle fort. is this
