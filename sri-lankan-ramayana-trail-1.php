                                    <ul class="fa-ul">
                                        <li><i class="fa-li fa fa-hand-o-right"></i>After breakfast, start your day by visiting the Thiru Koneswaram Temple.  After exploring the temple, proceed to the Kanniya Hot Wells</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>In the afternoon, continue your journey to Ramboda.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Overnight stay in Ramboda.</li>
                                    </ul>

                                    <p><b><em>Thiru Koneswaram Temple </em></b>holds historical and religious significance, as it was built by sage Agasthyar as per the instructions of Lord Shiva. It is believed that Lord Shiva was impressed by the devotion of King Ravana and rewarded him by having a temple built in his honor. Lord Rama also offered prayers here to rid himself of the Brahmahasthi Dosham.</p>

                                    <p><b><em>Kanniya Hot Wells, </em></b>according to legend, this is the place where King Ravana performed the last rites for his mother. When he couldn't find water to perform the rites, he became angry and pierced the ground with his trishul seven times. Water immediately started gushing out, and it is said that the temperature of the water varies in each of the seven wells, symbolizing the subsiding of the king's anger.</p>
                                </div>
                            </div>

                            <div class="item">
                                <h5 class="company-name"><span style="color:#6b52a3;">Day 05 &nbsp;&nbsp;</span>
                                    Ramboda | Ella</h5>
                                <div>
                                    <ul class="fa-ul">
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Breakfast at the hotel or Ashramam.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Visit Sri Baktha Hanuman Temple and proceed to Ella, enjoying the scenic journey.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Enroute, visit Gayathri Peedam, Sita Amman Temple and Divurumpola.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Overnight stay in Ella.</li>
                                    </ul>
                                    
                                    <p><b><em>Baktha Hanuman Temple, </em></b>Sri Baktha Hanuman Temple, built in honor of Hanuman, who on the hills of Ramboda, was searching for Setadevi.</p>

                                    <p><b><em>Gayathri Peedam: </em></b>Gayathripeedam is believed to be the place from where King Ravana's son Meghanath propitiated Lord Shiva with penance and pooja's and in turn was granted super natural powers by Lord Shiva.</p>

                                    <p><b><em>Sita Amman Temple, </em></b>there is a stream by the side of the temple. It is believed that Sitadevi bathed in the stream. It is interesting to note that footprints akin to Lord Hanuman's are found on the rocks along this river and some are of small size, and some are of large size, which indicating the immense power of Lord Hanuman transforming himself into any size.</p>
                                    
                                    <p><b><em>Divurumpola, </em></b>this is the place where Sitadevi underwent the "Agni" test.</p>
                                </div>
                            </div>

                            <div class="item">
                                <h5 class="company-name"><span style="color:#6b52a3;">Day 06 &nbsp;&nbsp;</span>
                                    Ella | Katharagama</h5>
                                <div>
                                    <ul class="fa-ul">
                                        <li><i class="fa-li fa fa-hand-o-right"></i>After breakfast, you will proceed to visit the Ravana Caves and Ravana Falls.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>In the afternoon, you will continue your journey to Katharagama. Upon arrival, you will have the opportunity to visit the Katharagama Temple.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Overnight stay in Katharagama.</li>
                                    </ul>
                                    
                                    <p><b><em>Ravana Cave: </em></b>These tunnels prove beyond doubt the architectural brilliance of King Ravana. These tunnels served as a quick means of transport through the hills and also as a secret passage. These tunnels networked all the important cities, airports and dairy farms.</p>

                                    <p><b><em>Katharagama Temple: </em></b>This is the temple of Lord Karthikeya Subramaniam at Kataragama. Lord Karthikeya was requested to go to the battlefield by Lord Indra on the last day of war. This was done to protect Lord Rama from the wrath of Brahmaasthram shot by King Ravana which otherwise would have weakened Lord Rama. Brahmaasthram aimed at Lord Rama for the second time was rendered useless by the presence of Lord Karthikeya</p>

                                </div>
                            </div>

                            <div class="item">
                                <h5 class="company-name"><span style="color:#6b52a3;">Day 07 &nbsp;&nbsp;</span>
                                    Katharagama | Colombo</h5>
                                <div>
                                    <ul class="fa-ul">
                                        <li><i class="fa-li fa fa-hand-o-right"></i>After breakfast, you will proceed to Colombo.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Enroute, you will visit Ussangoda, a unique geological formation known for its red soil and stunning coastal views.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Next, you will visit Devundara (seenigama), a famous temple located near the sea.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>You will also make a stop at Rumassala Sanjeevani Drops, a natural herbal garden known for its medicinal plants.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Finally, you will visit the Kelaniya Buddhist Temple, a sacred site with a rich history and beautiful architecture.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Overnight stay in Colombo.</li>
                                    </ul>

                                    <p><b><em>Ussangoda: </em></b>According to Ramayana, after meeting Sitadevi Lord Hanuman decided to test the strength of the mighty King Ravana and his army of Rakshasas. In the events that unfolded Lord Hanuman’s tail was set on fire by the rakshasas, who in turn went on to torch some parts of King Ravana’s empire. It is believed that Ussanggoda is one of the torched areas, which is said to have been an airport used by King Ravana.</p>

                                    <p><b><em>Rumassala Sanjeevani Drops: </em></b>During the war, both lord Rama and Lakshmana were hit by powerful arrows, fell unconscious and to bring them back to life Lord Hanuman was instructed to fetch the lifesaving herbs from Himalaya. Lord Hanuman went to Himalayas, lifted the whole hill and brought it, as he was not able to identify the lifesaving herbs alone. It is believed that parts of the hill fell on five places in Sri lanka, namely, Rumasala in Galle, Dolu Kanda in Hiripitiya, Ritigala on the Habarana Anuradhapura road, Thalladi in Mannar and Kachchativu in the north.</p>

                                    <p><b><em>Kelaniya Buddhist Temple: </em></b>It is believed that Kelaniya is the place from where King Vibeeshana ruled after being crowned by Lakshmana. As Lord Rama was in exile for 14 years, could not take part in any coronation ceremony, deputed his younger brother Prince Lakshmana to crown Vibeeshana.</p>
                                </div>
