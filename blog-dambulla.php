                                <p>The cave temple in Dambulla is the main draw.The underground network of tunnels and caves of the Dambulla Cave Temple dates back to the first century B.C. and is filled with statues, murals, and other religious imagery. A massive golden statue of Buddha marks the entrance to the Dambulla Museum and the caverns. a worthwhile visit to this gallery of wall art.Spend a few hours away from your schedule simply taking in the history of this historic location.</p>

