                                <h3>Dambulla</h3>
                                <p>Full of breathtaking natural, historic, and religious monuments. There are many incredible things to do in Dambulla, and not a beach in the vicinity.</p>
                                <p>Although Dambulla isn't the greatest spot to stay in Sri Lanka, it does offer a number of intriguing attractions.</p>

                                <h4>Let us look at the best attractions in Dambulla</h4>
                                <ul>
                                    <li><b>Sigiriya Rock Fortress</b></li>
                                    <li><b>Dambulla Cave Temple</b></li>
                                    <li><b>Pidurangala Rock</b></li>
                                    <li><b>Avkana Lord Buddha Statue</b></li>
                                    <li><b>Kaludiya Pokuna</b></li>
                                    <li><b>Yapahuwa Rock Fortress</b></li>
