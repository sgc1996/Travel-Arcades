                                <p>If you want a different perspective of Lion Rock, then you’ll find it at Pidurangala. This rocky outcrop offers a <b>perfect view of the fortress in the sky</b> and some pretty special photography opportunities too.</p>
                                <p>There are fewer tourists since Pidurangala is a more difficult climb because there isn't a sufficient stairway.Pidurangala is best enjoyed at sunrise when the horizon puts on a pretty spectacular show.</p>

