                                <p>Kalu Diya Pokuna, which is surrounded by Sri Lanka's unparalleled natural beauty, is another incredibly serene location. Trekking through the verdant woods will greet you with the waters of Kalu Diya Pokuna, a black pond.</p>
                                <p>The remnants of an old Buddhist monastery can be found at Kaludiya Pokuna (Black Water Pond), an archaeological forest site that was first populated about 200 BC.</p>

