                                <p>Yapahuwa Rock Fortress 100-meter-tall rock fortress is a treasure from the nation's medieval past, towering over the surrounding terrain. Though most tourists don't visit Yapahuwa because of its rich regional tradition, it is nevertheless well worth a visit.</p>
                                <p>Yapahuwa was the original location of the Buddha's Sacred Tooth Relic, which is currently housed at Kandy's Temple of the Tooth!</p>

