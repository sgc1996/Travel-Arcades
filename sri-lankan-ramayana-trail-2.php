                                    <p><b><em>Ussangoda: </em></b>According to Ramayana, after meeting Sitadevi Lord Hanuman decided to test the strength of the mighty King Ravana and his army of Rakshasas. In the events that unfolded Lord Hanuman's tail was set on fire by the rakshasas, who in turn went on to torch some parts of King Ravana's empire. It is believed that Ussanggoda is one of the torched areas, which is said to have been an airport used by King Ravana.</p>

                                    <p><b><em>Rumassala Sanjeevani Drops: </em></b>During the war, both lord Rama and Lakshmana were hit by powerful arrows, feel unconscious and to bring them back to life Lord Hanuman was instructed to fetch the lifesaving herbs from Himalaya. Lord Hanuman went to Himalayas, lifted the whole hill, and brought it, as he was not able to identify the lifesaving herbs alone. It is believed that parts of the hill fell on five places in Sri lanka, namely, Rumasala in Galle, Dolu Kanda in Hiripitiya, Ritigala on the Habarana Anuradhapura road, Thalladi in Mannar and Kachchativu in the north.</p>
