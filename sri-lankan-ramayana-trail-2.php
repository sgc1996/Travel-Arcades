                                    <p><b><em>Mini Adams Peak </em></b>is a popular tourist attraction, and it offers a relatively easy hike with breathtaking views of the surrounding tea plantations and lush green landscapes. The trail starts from Ella town and takes approximately 1-2 hours to reach the summit, depending on your pace. Once you reach the top, you will be rewarded with panoramic views of the Ella Gap and the iconic Ella Rock.</p>

                                    <p><b><em>Ravana Cave: </em></b>These tunnels prove beyond doubt the architectural brilliance of King Ravana. These tunnels served as a quick means of transport through the hills and as a secret passage. These tunnels networked all the important cities, airports, and dairy farms.</p>

                                    <p><b><em>Katharagama Temple: </em></b>This is the temple of Lord Karthikeya Subramaniam at Kataragama. Lord Karthikeya was requested to go to the battlefield by Lord Indra on the last day of war. This was done to protect Lord Rama from the wrath of Brahmaasthram shot by King Ravana which otherwise would have weakened Lord Rama. Brahmaasthram aimed at Lord Rama for the second time was rendered useless by the presence of Lord Karthikeya.</p>
