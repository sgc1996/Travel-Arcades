                                        <li><i class="fa-li fa fa-hand-o-right"></i>Upon arrival, you'll have the choice between a cycling or walking tour of the Polonnaruwa ancient city.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>In the afternoon, embark on an expedition to Minneriya National Park and behold the magnificence of its resident elephants while immersing yourself in the park's natural splendor.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Overnight stay in Habarana.</li>
                                    </ul>
                                    <p><b><em>Polonnaruwa, </em></b>located in the North Central Province of Sri Lanka, is an ancient city steeped in history and culture. It was the island nation's capital during the 11th to 13th centuries, serving as a vibrant center of power and civilization. Polonnaruwa is celebrated for its UNESCO-listed archaeological wonders, including grand palaces, intricately carved temples, colossal statues like Gal Vihara, and vast reservoirs such as Parakrama Samudra.</p>
                                    
                                    <p><b><em>Minneriya National Park, </em></b>is a haven for wildlife enthusiasts. This natural sanctuary is renowned for its remarkable "Gathering of Elephants" spectacle, where hundreds of Asian elephants congregate around the Minneriya Tank during the dry season. Beyond elephants, the park is home to diverse wildlife, including deer, monkeys, and a plethora of bird species. Minneriya National Park offers an unforgettable blend of wildlife encounters and scenic beauty.</p>
                                </div>
                            </div>

                            <div class="item">
                                <h5 class="company-name"><span style="color:#6b52a3;">Day 06 &nbsp;&nbsp;</span>
                                    Habarana | Sigiriya | Habarana</h5>
                                <div>
                                    <ul class="fa-ul">
                                        <li><i class="fa-li fa fa-hand-o-right"></i>After breakfast, you'll head to the iconic Sigiriya Rock Fortress to marvel at the ancient frescoes. (Traveling time approximately 30 minutes)</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Following your visit to Sigiriya, it's time for some well-deserved relaxation. You have the option to choose between a soothing foot massage or a rejuvenating full-body massage at a reputable spa in the area.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Overnight stay in Habarana.</li>
                                    </ul>
                                    <p>(Note: This itinerary provides an optional massage experience. If you prefer to skip the massage, you can utilize this time for additional exploration or relaxation according to your preferences.)</p>
                                    
                                    <p><b><em>Sigiriya Rock Fortress, </em></b>a UNESCO World Heritage Site in Sri Lanka, stands as a breathtaking testament to ancient architecture and artistry. Rising dramatically from the plains, it features captivating frescoes, intricate water gardens, and a monumental entrance guarded by lion's paws. This historic marvel, once a royal palace and Buddhist monastery, offers panoramic vistas and a glimpse into Sri Lanka's rich heritage.</p>
                                </div>
                            </div>

                            <div class="item">
                                <h5 class="company-name"><span style="color:#6b52a3;">Day 07 &nbsp;&nbsp;</span>
                                    Habarana | Kandy</h5>
                                <div>
                                    <ul class="fa-ul">
                                        <li><i class="fa-li fa fa-hand-o-right"></i>After breakfast, embark on a scenic journey from Habarana to the cultural capital of Sri Lanka, Kandy. (Traveling time approximately 3 hours)</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Check into your hotel and take a moment to refresh.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>In the evening, visit the renowned Temple of the Tooth Relic and experience the vibrant and rhythmic traditions of Kandy by attending a traditional Kandyan dance performance.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Overnight stay in Kandy.</li>
                                    </ul>
                                    <p><b><em>The Temple of the Tooth Relic, </em></b>situated in Kandy, Sri Lanka, is a revered UNESCO World Heritage Site and one of Buddhism's holiest shrines. It enshrines a sacred relic believed to be a tooth of Lord Buddha, making it a significant pilgrimage site for Buddhists worldwide. Encased within an ornate golden casket, the tooth relic is housed in a magnificent temple complex with intricate Kandyan architecture. The temple's serene surroundings and cultural importance make it a mesmerizing and spiritually significant destination in the heart of Sri Lanka.</p>
                                </div>
                            </div>

                            <div class="item">
                                <h5 class="company-name"><span style="color:#6b52a3;">Day 08 &nbsp;&nbsp;</span>
                                    Kandy | Ella</h5>
                                <div>
                                    <ul class="fa-ul">
                                        <li><i class="fa-li fa fa-hand-o-right"></i>After breakfast, embark on a breathtaking train journey from Kandy to Ella, where lush tea plantations and misty hillsides unfold before your eyes. (Traveling time approximately 1.45 hours)</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>On Arrival, take a short tuk-tuk ride to the Nine Arch Bridge to witness the iconic train bridge and its stunning surroundings.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Check in to the hotel and relax for an exciting day ahead.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Overnight stay in Ella.</li>
                                    </ul>
                                    <p><b><em>The Nine Arch Bridge, </em></b>located in Ella, Sri Lanka, is an architectural marvel nestled in the lush green hills. This iconic railway bridge, built during British colonial rule, consists of nine magnificent arches, blending seamlessly with the natural surroundings. The bridge has become a popular tourist attraction, especially when trains pass over it, offering stunning photo opportunities amidst the stunning tea plantation landscape. It's a testament to both engineering excellence and the beauty of Sri Lanka's hill country.</p>
                                </div>
                            </div>

                            <div class="item">
                                <h5 class="company-name"><span style="color:#6b52a3;">Day 09 &nbsp;&nbsp;</span>
                                    Ella</h5>
                                <div>
                                    <ul class="fa-ul">
                                        <li><i class="fa-li fa fa-hand-o-right"></i>After breakfast, embark on a trek to Mini Adam's Peak, a relatively easy hike with stunning views.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>In the afternoon, set out to explore two of Ella's captivating gems; the mystical Ravana Caves and the majestic Ravana Falls.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Overnight in Ella.</li>
                                    </ul>
                                    <p><b><em>Mini Adams Peak, </em></b>located in Ella, Sri Lanka, is a charming hill that beckons travelers with its gentle ascent and panoramic rewards. This trekking gem offers unobstructed views of the surrounding landscapes, featuring rolling hills, lush valleys, and the picturesque Ella Gap. A favorite among nature enthusiasts and photographers, this hike provides a delightful immersion in Sri Lanka's beauty of the hill country.</p>
                                    
                                    <p><b><em>Ravana Cave and Ravana fall </em></b>are sites rich in legend and history. It is believed to be the hideout of the mythical King Ravana from the epic Ramayana. As you explore the dark chambers and winding passages, you can sense the aura of ancient tales. Ravana Falls, on the other hand, is a breathtaking natural wonder in the same region. This powerful waterfall plunges from a height, creating a mesmerizing cascade that entices visitors to cool off in its refreshing pool or simply admire its beauty.</p>
                                </div>
                            </div>

                            <div class="item">
                                <h5 class="company-name"><span style="color:#6b52a3;">Day 10 &nbsp;&nbsp;</span>
                                    Ella | Galle</h5>
