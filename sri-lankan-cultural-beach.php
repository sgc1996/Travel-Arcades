                                        <li><i class="fa-li fa fa-hand-o-right"></i>After breakfast, it's time to set forth on your journey to the captivating city of Galle. (Traveling time approximately 3 hours)</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>On arrival, explore Galle Fort, a UNESCO World Heritage Site.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Check in to the hotel and relax on the sandy shores.</li>
                                        <li><i class="fa-li fa fa-hand-o-right"></i>Overnight stay in Galle.</li>
                                    </ul>
                                    <p><b><em>Galle, </em></b>a coastal city in southern Sri Lanka, features a rich tapestry of history and culture. Its centerpiece is Galle Fort, a UNESCO World Heritage Site, where centuries-old Dutch colonial architecture intertwines with vibrant street life. Explore cobbled streets, visit historic churches, and indulge in boutique shopping. The city is surrounded by stunning beaches, and its blend of tradition and modernity makes it a captivating destination.</p>
                                </div>
