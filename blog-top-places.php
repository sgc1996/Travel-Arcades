                                <p>Polonnaruwa is  a major tourist attraction, also a major city located in the north central plain of Sri Lanka.The capital of the Polonnaruwa District in Sri Lanka's North Central Province is Polnaruwa. The other portion of Polonnaruwa still exists as the regal ancient city of the Kingdom of Polonnaruwa, and is also referred to as New Town.</p>
                                <p>After Anuradhapura was destroyed in 993 AD, the capital of Sri Lanka was Polonnaruwa, the country's second-oldest monarchy. King Parakramabahu built it.Rich in archeological discoveries, the city is scattered with ruins, temples, shrines, and palaces. Investigate the royal palace from King Parakramabahu's era.</p>
                                <p>One of the main draws of Sri Lanka is the revered Quadrangle. The Vatadage, a circular relic house with an amazing 18-meter diameter and magnificent sculptural design that includes four exquisitely preserved stone Buddhas, is the focal point of this, Polonnaruwa's most concentrated section of ruins.The city is also home to the remnants of several remarkable dagobas, such as the 55-meter-tall Rankot Vihara made of red brick, the Menik Vihara, the Pabalu Vehera, and the Kiri Vihara, which was initially constructed for one of Parakramabahu's wives and covered in lime plaster.Inside the Alahana Pirivena, the former royal cremation grounds, is the spectacular cathedral-like Lankatilaka temple, whose walls soar to an amazing 17 meters. Inside, there is a colossal headless Buddha carved out of the tall end wall.</p>

