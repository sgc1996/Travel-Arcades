                                <h4>Ella</h4>
                                <p>Province: Uva Province</p>
                                <p>District: Badulla</p>
                                <p>Ella is a tiny town under the municipal council of the Badulla District in the Uva Province of Sri Lanka. At an elevation of 1,041 meters (3,415 feet) above sea level, it is located around 200 kilometers (120 km) east of Colombo.</p>
                                <p>Relaxed Ella, with its mild environment, tea plantations, and mountain forests, entices visitors to the highlands of Sri Lanka. Along with hidden temples, cascades, and caverns, the wildlife-rich area is also home to the Ella Gap, which provides panoramic views between a split in two hills. Situated amidst picturesque verdant hills and cascading waterfalls in the misty highlands of Sri Lanka is Ella, a highly desirable location on this tear-shaped island.</p>
                                <p>Many of tourists favorite Sri Lankan experiences can be found right here, including climbing Ella Rock's towering heights, seeing the enigmatic Nine Arch Bridge for the first time, standing on the brink of the nation's second-tallest waterfall, and riding the renowned Kandy to Ella train through tea plantations and mountain passes.</p>
                                <p>Ella is a quaint, laid-back village that is ideal for strolling, hiking, and getting away from the intense heat of the southern coast. Food from Sri Lanka is a must-try.</p>

