                                <h4>Dambulla</h4>
                                <p>Province: Central Province</p>
                                <p>District: Matale</p>
                                <p>Dambulla is a town in Central Province, Sri Lanka, located in the northern part of Matale District. In the Matale District, it is the second most populous and urbanized center after Matale.Dambulla, a thriving town near the center of Sri Lanka's Cultural Triangle, is well-known for its five cave temples, which are home to more than 150 outstanding pieces of art. The five caves are lavishly decorated with elaborate murals and statues, representing the pinnacle of Sinhalese Buddhist art. The caves are recognized as a UNESCO World Heritage Site.</p>
                                <p>Dambulla Cave Temples: UNESCO world Heritage</p>
                                <p>This cave monastery, with its five sanctuaries, has been a revered pilgrimage site for 22 centuries and is the largest and best-preserved cave-temple complex in Sri Lanka. Both the 157 statues and the 2,100 m2 of Buddhist mural paintings are extremely significant.</p>
                                <p>The cave temples of Dambulla are a collection of exquisitely furnished, varied-sized tunnels. Nearly every square inch of the sloping tunnels, which house several sculptures of the Buddha and his disciples as well as other kings who were engaged in the construction of the monument, is covered in murals. Some of Sri Lanka's best Buddhist artwork can be seen in the caves.</p>

                                <h6>Cavern 1</h6>
                                <p>The Devalaya Viharaya, also known as the Temple of the Gods, gets its name from Vishnu, whose pictures are painted on the walls. There is a stunning 14-meter-long sleeping Buddha within that was carved out of the rock; gold traces can be seen on his elbow, though they are frequently covered. Within the cave are other figures, including Ananda, the disciple of the Buddha.</p>

                                <h6>Cavern 2</h6>
                                <p>Vattagamini and Nissankalla are the two monarchs who built the Maharaja Vihara, also known as the Temple of the Great monarchs. They are both shown as sculptures. Apart from its remarkable dimensions of up to 50 meters in length and 7 meters in height, the cave is brimming with Buddha and Hindu god statues and is beautifully painted.</p>

                                <h6>Cavern 3</h6>
                                <p>The Great New Temple, known as the Maha Aluth Viharaya, is believed to have been built by King Kirti Sri Rajasinha. Over fifty sitting and standing Buddhas, one of the king, and two more Buddha statues—one sleeping, the other meditating—carved out of sheer rock are positioned below a height of about ten meters. The 18th-century murals that cover the cave's walls and ceiling are equally stunning. They were created in this location.</p>

                                <h6>Cavern 4</h6>
                                <p>One of Dambulla's smaller caverns, the Paccima Viharaya (Western Cave) is nonetheless crammed with Buddha sculptures and features a dagoba in the middle. In addition to florals and decorative checks, Buddhas are also portrayed in the wall paintings.</p>

                                <h6>Cavern 5</h6>
                                <p>The Devana Aluth Viharaya (Second New Temple), the smallest and most recent cave, has murals of Vishnu, Kataragama, and the local god Bandara in addition to a 10-meter-tall reclining Buddha. These statues are made of brick and plaster, while the most of the statues in Dambulla are carved out of solid rock.</p>

