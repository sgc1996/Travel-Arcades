                                <h4>Sigiriya Rock Forest</h4>
                                <p>Province: Northern Central Province</p>
                                <p>District: Dambulla</p>
                                <p>UNESCO world Heritage</p>
                                <p>Sigiriya, also known as Sinhagiri, is a historic rock stronghold in Sri Lanka's Central Province that is situated close to the town of Dambulla in the northern Matale District. This historically and archaeologically significant location is dominated by a gigantic granite column that rises to a height of around 590 feet.</p>
                                <p>On the numerous acres of land at the top, the Sinhalese monarch Kashyapa I (r. 477–495) constructed a palace shaped like a massive lion with the intention of using it as a defense against his foes. But in 495, the king was vanquished, and the palace was destroyed. But the location quickly gained popularity as a pilgrimage site, and it is now a well-liked tourist attraction. Through the lion's gaping paws, one of the few parts of the palace still standing, visitors start their last ascent to the summit. 21 rock paintings of apsaras, or celestial singers and dancers, are also noteworthy. In 1982, Sigiriya received the UNESCO World Heritage designation.</p>

