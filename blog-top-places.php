                                <h4>Kandy</h4>
                                <p>Province: Northern Central Province</p>
                                <p>District: Kandy</p>
                                <p>UNESCO: world Heritage</p>
                                <p>Situated in the Central Province of Sri Lanka lies a Large city called Kandy. It served as the final capital of Sri Lanka during the reign of the old kings. Situated amidst rolling hills on the Kandy plateau, the city spans a region of tropical plantations, primarily cultivating tea.</p>

                                <h6>Sacred City of Kandy</h6>
                                <p>Senkadagalapura, also known as the sacred Buddhist shrine, served as the final capital of the Sinhala rulers, whose patronage allowed the Dinahala civilization to flourish for over 2,500 years until the British takeover of Sri Lanka in 1815. In addition, it is home to the well-known pilgrimage site, the Temple of the Tooth Relic, which houses the Buddha's precious tooth. The two main sects of Buddhism, Mahayana and Theravada, both established centers in Kandy. The most significant of its several Buddhist temples is Dalada Maligawa, also known as the "Temple of the Tooth," where a priceless artifact believed to be the Buddha's left upper canine has been conserved since 1590. Under the reigns of the Kandyan kings, the temple was built between 1687 and 1707 and between 1747 and 1782. It is attached to a tower (1803) that was formerly a jail and now holds a significant collection of manuscripts written in palm leaf. After Tamil rebels bombed the temple in January 1998, causing damage to its roof and facade, rehabilitation work started right away. Two notable 14th-century Hindu and Buddhist temples southwest of Kandy are the Gadaladeniya Vihare and the Lankatilaka Vihare.</p>

