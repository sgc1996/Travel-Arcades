                                    <p><b><em>Horton Plains National Park, </em></b>located in the central highlands of Sri Lanka, is a UNESCO World Heritage Site renowned for its unique landscape and biodiversity. Covering about 3,169 hectares, it features dramatic escarpments, rolling grasslands, and lush montane forests. The park's highlights include "World's End," a steep precipice offering breathtaking views, and "Baker's Falls," a picturesque waterfall. Visitors can explore its pristine environment, encounter diverse flora and fauna, and enjoy hiking trails amidst the cool, misty surroundings. Horton Plains is a natural treasure offering a glimpse into Sri Lanka's distinctive highland ecosystems.</p>
