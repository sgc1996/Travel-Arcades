                                <h4>Unawatuna Beach</h4>
                                <p>The most "famous" beach in Sri Lanka is Unawatuna, which is also one of the most popular travel attractions there. With green palm palms all around, the beach has a wonderful banana shape with golden sand and turquoise sea!It was our first trip to a beach in Sri Lanka. We opted to stay at a guesthouse a few kilometers away rather than one of the posh and pricey hotels along Unawatuna's beach. Two different day trips later, we strolled to the Unawatuna Beach.</p>
                                <p>Unawatuna Beach offers a plethora of thrilling water sports for adventure seekers to enjoy. Don your snorkeling gear and venture into the underwater realm teeming with vibrant coral reefs and a diverse array of tropical species. Embrace the challenge by grabbing a surfboard and hitting the waves. Another enjoyable and soothing activity to try is paddleboarding. Jet skiing, kayaking, and banana boat trips are provided for people who enjoy speed and thrill. Unawatuna Beach is a water lover's paradise with its warm water and an abundance of marine life.</p>

