                                <h4>Mirissa Beach</h4>
                                <p>Located in the Matara District of the Southern Province, Mirissa is a tiny town on Sri Lanka's south coast.With good cause, Mirissa is one of the most well-liked beach resorts in southern Sri Lanka! Everything is there: stunning Sri Lankan beaches, excellent surfing waves, and waterways abounding with marine life, including large blue whales and turtles. Eat delectable meals from coastal eateries and spend your days lounging in your hammock and sucking on fresh coconuts.</p>

                                <h6>The top beaches in Mirissa for breathtaking surfing and sunsets are listed below</h6>
                                <p><b>Secret Beach Mirissa</b></p>
                                <p>One of the most beautiful beaches in Mirissa is Secret Beach. It was once a secret location known only by the locals but has now become a popular tourist destination. The beach is ideal for swimming and sunbathing.</p>

