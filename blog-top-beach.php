                                <h3>Top Beach To Visit In Sri Lanka</h3>
                                <p>Sri Lanka’s tropical beaches are unanimously adored. Crafted from a 1340km (833-mile) coastline, its countless topographically varied beaches, coves and bays attract sun-seekers, fun lovers and surfers throughout the year. Wildlife plays its part too – beaches are vital nesting grounds for endangered marine turtles, while blue whales are sighted at sea.</p>
                                <p>Sri Lanka is a year-round destination, but it's influenced by two opposing monsoons that affect the weather, ocean currents and when and where you should visit. Generally speaking, the southwest coast is best between November and April, and the east from May to September. These are the best beaches in Sri Lanka.</p>

