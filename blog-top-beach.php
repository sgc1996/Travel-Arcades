                                <h4>Kalkudah Beach</h4>
                                <p>In the Batticaloa District of Sri Lanka, approximately 35 kilometers northwest of Batticaloa, is the beach vacation town known as Kalkudah or Kalkuda.There can't be too many locations on Earth where you can stroll to two magnificent beaches. We have located two beach resorts in Sri Lanka that meet those requirements! The beaches of Pasikuda and Kalkudah are on the less populated East Coast. It was difficult for us to leave behind two breathtakingly beautiful back-to-back expanses of sand.</p>
                                <p>Luxurious accommodations surround the magnificent white sands of Passikudah Beach, which is formed like a curve and located on the northern border of the coast. Before the tsunami struck Kalkudah Beach, the shore was lined with numerous resorts and hotels catering to tourists. The beach has been abandoned since the tsunami, with the exception of a few dogs and cows, as well as sporadic boats and fishermen. If travelers are seeking a more private beach surrounded by breathtaking scenery, Kalkudah Beach need to be on their itinerary.</p>

