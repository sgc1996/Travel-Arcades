                                <h4>Bentota Beach</h4>
                                <p>Bentota is a seaside town in the Southern Province of Sri Lanka, specifically in the Galle District.Bentota is located at an elevation of 3 meters (9.8 feet) above sea level on the southern side of the mouth of the Bentota River.</p>
                                <p>Beautiful beach at Bentota. A warm sea softly shelves into fine sand. There weren't many people on the beach for the majority of the day, but on the weekends, residents with their families visit for a swim, and the mood is wonderful and welcoming. There are some powerful currents, so DO pay attention to the caution flags. Swimming is safer near the Taj hotel on the north side where the beach dog legs lead south. You can stroll for miles while listening to the fisherman sing as they fold their nets and retrieve outriggers from the ocean.There's always a nice breeze, so be careful because the sun is reflected off the sand and you can burn easily.</p>

